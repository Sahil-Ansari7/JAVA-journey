import java.util.*;
public class Basics {
    public static void main(String[] args) {
        System.out.println("By using &(and) operator : " + (5 & 6));
        System.out.println("By using |(or) operator : " + (5 | 6));
        System.out.println("By using ^(XOR) operator : " + (5 ^ 6));
        System.out.println("By using ~(complement) operator : " + (~5));
        System.out.println("By using <<(binary left shift) operator : " + (5 << 2));
        System.out.println("By using >>(binary right shift) operator : " + (6 >> 1));

    }
}
